// Operador Ternario

let resultado = 10 > 2 ? "Verdadero" : "Falso";
console.log(resultado);

resultado = 1 > 2 ? true : false;
console.log(resultado);

// Saber si un número es par o impar con el operador ternario.
let numero = 2;
resultado = numero % 2 == 0 ? "Número Par" : "Número Impar";
console.log(resultado);
