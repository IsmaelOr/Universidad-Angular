// Estructura Switch
let numero = 4;
let numeroTexto;

switch (numero) {
  case 1:
    numeroTexto = "Uno";
    break;
  case 2:
    numeroTexto = "Dos";
    break;
  case 3:
    numeroTexto = "Tres";
    break;
  case 4:
    numeroTexto = "Cuatro";
    break;
  default:
    numeroTexto = "Sin coincidencia";
}

console.log(numeroTexto);
