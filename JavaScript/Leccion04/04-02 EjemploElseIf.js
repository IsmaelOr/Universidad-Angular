// Ejemplo con Sentencia if-else
// Introducir un número y si el número se encuentra en las opciones decir su nombre, si no decir número desconocido

let numero = 3;

if (numero == 1) {
  console.log("Uno");
} else if (numero == 2) {
  console.log("Dos");
} else if (numero == 3) {
  console.log("Tres");
} else if (numero == 4) {
  console.log("Cuatro");
} else {
  console.log("Número desconocido");
}
